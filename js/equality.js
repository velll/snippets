// a equal to b
a == b

// a equal to b AND type of a is equal to type of b
a === b

// Examples:

0 == false
// -> true

0 === false
// -> false


// Negation

// a is not equal to b
a != b

// a is not equal to b OR type of a is not equal to type of b
a !== b

0 != false
// -> false (they are actually equal)

0 !== false
// -> true (they don't have the same type)
